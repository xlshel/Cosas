    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Organizador organizador = new Organizador();
        organizador.setCedula(jTextField1.getText());
        organizador.setNombres(jTextField2.getText());
        organizador.setApellidos(jTextField3.getText());
        organizador.setNombreUsuario(jTextField4.getText());
        organizador.setPassword(jPasswordField1.getText());
        
        Organizador organizadorB = Organizador.buscarOrganizador(organizador.getCedula());
        if (organizadorB.getCedula().equals(organizador.getCedula())) {
            JOptionPane.showMessageDialog(null, "Organizador ya esta registrado");
        } else {
            boolean respuesta = Organizador.registrarOrganizador(organizador);
            if (respuesta) {
                JOptionPane.showMessageDialog(null, "Registro exitoso");
                //vuelve a la pantalla de inicio
                new GuiInicioSeccion().setVisible(true);
            
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

