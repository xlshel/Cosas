    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String codigobuscar = jTextField1.getText();
        ArrayList<Curso> cursos = Curso.obtenerCursos();
        boolean encontrado = false;
        for (Curso curso : cursos) {
            if (codigobuscar.equals(curso.getCodigo())) {
                jTextField2.setText(curso.getNombre());
                jTextField3.setText("" + curso.getSemestre());
                ArrayList<Estudiante> estudiantes = curso.getEstudiantes();
                for(Estudiante est: estudiantes){
                    Object fila[] = {est.getIdentificacion(), curso.getNombre(), curso.getSemestre()};
                    modelo.addRow(fila);
                }
                encontrado = true;
                break;
            }
            
        }
              if(encontrado == false){
                JOptionPane.showMessageDialog(null, "Curso no encontrado");
            } 
    }//GEN-LAST:event_jButton1ActionPerformed

